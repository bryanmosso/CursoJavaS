//Ejercicio para numero a texto

let numero=3
let textoNumero;

switch(numero){
    case 1:
        textoNumero= "Numero uno";
        break;
    
    case 2:
        textoNumero="Numero dos";
        break;

    case 3:
        textoNumero= "Numero tres";
        break;

    case 4:
        textoNumero= "Numero cuatro";
        break;

    default: textoNumero="Error";
}

console.log(textoNumero);